// \sqsupseteq
pub const UNICODE_SQSUPSETEQ: &str = "\u{2292}";

// \subseteq
pub const UNICODE_SUBSETEQ: &str = "\u{2286}";

// \supseteq
pub const UNICODE_SUPSETEQ: &str = "\u{2287}";

// \forall
pub const UNICODE_FORALL: &str = "\u{2200}";

